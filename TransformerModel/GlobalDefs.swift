import Foundation

// Important value #1: pi
let pi:Double = 3.1415926535897932384626433832795

// Permeability of vacuum
let µ0:Double = pi * 4.0E-7

// Speed of light
let c:Double = 299792458.0 // m/s

// Permittivity of free space
let ε0:Double = 1 / (µ0 * c * c) // Farads/m



